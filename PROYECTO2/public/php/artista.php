    function mostrarDiscos(int $id_artista) {
        global $discos;
        echo '
        <div class="discos">
        ';
        foreach ($discos[$id_artista] as $id_disco => $nombreDisco) {
            echo '
            <table class="col-">
            <caption>'.$nombreDisco.'
            </caption>
            ';
            mostrarCanciones($id_artista, $id_disco);
            echo '</table>';
        }
        echo '</div>';
    }

